private void btnReturnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReturnRegisterActionPerformed
// TODO add your handling code here:
    try{
      String errorMsg = "";
        if("".equals(txtReturnName.getText())){
          errorMsg += "Return Name cannot be empty \n";
        }
        if("".equals(txtItemReturnQuantity.getText())){
          errorMsg +=  "Item Quantity cannot be empty \n";
        }
//        if("".equals(itemReturnDate.getDate())){
//          errorMsg += "Date cannot be empty \n";
//        }
        else{
            Validation v = new Validation();
            
            if (!v.checkValidation("text", txtReturnName.getText()))
                errorMsg += "Return Name supports only numbers \n";   
            
            if (!v.checkValidation("number", txtItemReturnQuantity.getText()))
                errorMsg += "Item Quanity supports only numbers \n";   
                                             
        }
      if(errorMsg.isEmpty()){ 
          
        rturn = new ReturnOF();
        rturn.setReturn_id(0);
        rturn.setReturn_name( txtReturnName.getText() );
        
        Date rd = itemReturnDate.getDate();
        SimpleDateFormat r_date =new SimpleDateFormat("yyyy-MM-dd");
        rturn.setReturn_date(r_date.format(rd).toString());

        //rturn.setReturn_date(itemReturnDate.getDateFormatString());      
        rturn.setReturn_quantity(Integer.parseInt(txtItemReturnQuantity.getText()));          
        rturn.setItem_name(cmbItemName.getSelectedItem().toString());
        blRturn = new ReturnBl(rturn);
        if(blRturn.insertRturn()){
          JOptionPane.showMessageDialog(this, "Return Item Created Successfully", "Operation Successful", JOptionPane.INFORMATION_MESSAGE);
        }else{
          JOptionPane.showMessageDialog(this, "Unable to add Item Return", "Error Occured", JOptionPane.ERROR_MESSAGE);
        }
      }else{
          JOptionPane.showMessageDialog(this, errorMsg, "Error Occured", JOptionPane.ERROR_MESSAGE);
      }
      
    }catch(Exception ex){
      JOptionPane.showMessageDialog(this, ex.getMessage(), "Error Occured", JOptionPane.ERROR_MESSAGE);
    }
}//GEN-LAST:event_btnReturnRegisterActionPerformed

